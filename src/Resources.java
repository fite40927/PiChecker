import java.util.*;

public class Resources {
	public static Scanner scan = new Scanner(System.in);
	public static String print(String s) {
		System.out.print(s);
		return s;
	}
	public static String print(int s) {
		System.out.print(s);
		return Integer.toString(s);
	}
	public static String print(double s) {
		System.out.print(s);
		return Double.toString(s);
	}
	public static Object print(Object s) {
		System.out.print(s);
		return s;
	}
	public static char print(char s) {
		System.out.print(s);
		return s;
	}
	
	public static String println(String s) {
		System.out.println(s);
		return s;
	}
	public static String println(int s) {
		System.out.println(s);
		return Integer.toString(s);
	}
	public static String println(double s) {
		System.out.println(s);
		return Double.toString(s);
	}
	public static Object println(Object s) {
		System.out.println(s);
		return s;
	}
	public static char println(char s) {
		System.out.println(s);
		return s;
	}
}
